function isUniform(arr) {
  var first = arr[0];
  for (var i = 0; i < arr.length; i++)
    if (arr[i] !== first) {
      return false;
    }
  return true;
}

console.log(isUniform([1, 1, 1, 1]));
console.log(isUniform([2, 1, 1, 1]));
console.log(isUniform(['a', 'b', 'p']));
console.log(isUniform(['b', 'b', 'b']));
